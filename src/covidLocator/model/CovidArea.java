package covidLocator.model;


public class CovidArea {
private int area_id;
private String area_name;
private double area_x;
private double area_y;
public int getArea_id() {
	return area_id;
}
public void setArea_id(int area_id) {
	this.area_id = area_id;
}
public String getArea_name() {
	return area_name;
}
public void setArea_name(String area_name) {
	this.area_name = area_name;
}
public double getArea_x() {
	return area_x;
}
public void setArea_x(double area_x) {
	this.area_x = area_x;
}
public double getArea_y() {
	return area_y;
}
public void setArea_y(double area_y) {
	this.area_y = area_y;
}
}
