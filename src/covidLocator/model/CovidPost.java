package covidLocator.model;

public class CovidPost {
private int post_id;
private int post_user;
private int post_area;
private String post_title;
private String post_content;
private String post_nickname;
private int post_view;
private String post_date;
public int getPost_id() {
	return post_id;
}
public void setPost_id(int post_id) {
	this.post_id = post_id;
}
public int getPost_user() {
	return post_user;
}
public void setPost_user(int post_user) {
	this.post_user = post_user;
}
public int getPost_area() {
	return post_area;
}
public void setPost_area(int post_area) {
	this.post_area = post_area;
}
public String getPost_title() {
	return post_title;
}
public void setPost_title(String post_title) {
	this.post_title = post_title;
}
public String getPost_content() {
	return post_content;
}
public void setPost_content(String post_content) {
	this.post_content = post_content;
}
public String getPost_nickname() {
	return post_nickname;
}
public void setPost_nickname(String post_nickname) {
	this.post_nickname = post_nickname;
}
public int getPost_view() {
	return post_view;
}
public void setPost_view(int post_view) {
	this.post_view = post_view;
}
public String getPost_date() {
	return post_date;
}
public void setPost_date(String post_date) {
	this.post_date = post_date;
}
}