package covidLocator.model;

public class CovidAlert {
private int alert_id;
private int alert_area;
private String alert_title;
private String alert_chk;
private String alert_date;
public int getAlert_id() {
	return alert_id;
}
public void setAlert_id(int alert_id) {
	this.alert_id = alert_id;
}
public int getAlert_area() {
	return alert_area;
}
public void setAlert_area(int alert_area) {
	this.alert_area = alert_area;
}
public String getAlert_title() {
	return alert_title;
}
public void setAlert_title(String alert_title) {
	this.alert_title = alert_title;
}
public String getAlert_chk() {
	return alert_chk;
}
public void setAlert_chk(String alert_chk) {
	this.alert_chk = alert_chk;
}
public String getAlert_date() {
	return alert_date;
}
public void setAlert_date(String alert_date) {
	this.alert_date = alert_date;
}

}
